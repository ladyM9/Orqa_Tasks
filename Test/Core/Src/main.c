void GetValue(struct Value v, uint32_t _ch)
{
	HAL_ADCEx_Calibration_Start(&hadc3, _ch, ADC_SINGLE_ENDED); //kalibracija ADC-a

	for(int i = 0; i < 6 ; i++)
	{
		//HAL_ADCEx_Calibration_Start(&hadc3,ADC_SINGLE_ENDED);

		ch.Channel = _ch;
		ch.Rank = ADC_CHANNEL_0_NUMBER;
		HAL_ADC_ConfigChannel(&hadc3, &ch);
		HAL_ADC_Start(&hadc3); //kreni sa citanjem na podensenom ADC kanalu
		HAL_ADC_PollForConversion(&hadc3, 1000);
		v.adcX[i] = HAL_ADC_GetValue(&hadc3); //ocitanu vrijednost spremi u polje adcX
		HAL_ADC_Stop(&hadc3); //zaustavi citanje ADC-a
		ch.Rank = ADC_REGULAR_RANK_1;
		HAL_ADC_ConfigChannel(&hadc3, &ch);


	}
	 for(int i = 0; i < 6; i++)
	 {

		 v.len = sniprintf(v.buffervrij, sizeof(v.buffervrij), "Vrijednost X %d\n\r ", v.adcX[i]);
		 HAL_UART_Transmit(&huart3, (uint8_t *)v.buffervrij, v.len, 1000); //posalji ocitanu vrijednost na UART
	 }



	 v.NewAvrg = 0;
	 v.pos = 0; //pocetna pozicija u arrNum
	 //v.NewAvrg; = 0; //newArg je izracunati moving average od uzetog niza brojeva
	 v.sum = 0; //suma je u pocetku jednaka 0, jer jos nista nismo zbrojili
	 v.lenMoving_Average = sizeof(v.arrNum)/ sizeof(int);
	 v.count = sizeof(v.adcX) / sizeof(int);


	 for(int i = 0; i < v.count; i++)
	 {

		 v.NewAvrg= movingAvg(v.arrNum, &v.sum, v.pos, v.lenMoving_Average, v.adcX[i]);
		 v.lenMoving_Average = sniprintf(v.moving_avg_buff, sizeof(v.moving_avg_buff), "Novi moving average za X je  %d ", v.NewAvrg);
		 HAL_UART_Transmit(&huart3, (uint8_t *)v.moving_avg_buff, v.lenMoving_Average, 1000);
		 v.pos++;
		 if(v.pos >= v.lenMoving_Average)
		 {
			 v.pos = 0;
		 }

	 }
	 HAL_Delay(1000);

}


void GetYvalue(struct ValueY vy, uint32_t ch2)
{

	HAL_ADCEx_Calibration_Start(&hadc1, ch2, ADC_SINGLE_ENDED);
	for( int i = 0; i < 6; i++)
	{
		ch.Channel = ch2;
		ch.Rank = ADC_CHANNEL_6_NUMBER;
		HAL_ADC_ConfigChannel(&hadc1, &ch);
		//HAL_ADC_ConfigChannel(&hadc3, &ch2);
		HAL_ADC_Start(&hadc1); //Y OS
		HAL_ADC_PollForConversion(&hadc1, 1000);
		vy.adcY[i] = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);
		ch.Rank = ADC_REGULAR_RANK_1;
		HAL_ADC_ConfigChannel(&hadc1, &ch);
	}

	for(int i = 0; i < 6; i++)
	{
		vy.leny = sprintf(vy.buffer , "Vrijednost Y je : %d\n\r ", vy.adcY[i]);
		HAL_UART_Transmit(&huart3, (uint8_t *)vy.buffer, vy.leny, 1000);
	}

	vy.sumY = 0;
	vy.pos = 0;
	vy.NewAvrgY = 0;
	vy.lenMoving_AverageY = sizeof(vy.arrNumY)/sizeof(int);
	vy.countY = sizeof(vy.adcY)/sizeof(int);

	for(int i = 0; i < 6; i++)
	{
		vy.NewAvrgY = movingAvg(vy.arrNumY, &vy.sumY , vy.pos, vy.lenMoving_AverageY, vy.adcY[i]);
		vy.lenMoving_AverageY = sniprintf(vy.moving_avg_buffY, sizeof(vy.moving_avg_buffY), "Novi moving average za Y je %d ", vy.NewAvrgY);
		HAL_UART_Transmit(&huart3, (uint8_t *)vy.moving_avg_buffY,  vy.lenMoving_AverageY, 1000);
		vy.pos++;
		if(vy.pos >= vy.lenMoving_AverageY)
		{
			vy.pos = 0;
		}
	}



	 HAL_Delay(1000);
}

int movingAvg(int *p, long *ptrsum, int pos, int len, int nextNum)
{
	*ptrsum = *ptrsum - p[pos] + nextNum; //na ovaj nacin oduzimam najstariji broj od prethodnog zbroja niza, nakon oduzimanja dodam novi broj
	p[pos] = nextNum; //dodajem trenutnoj poziciji novi broj

	return *ptrsum/len; //vraca izracunati prosjek
}
